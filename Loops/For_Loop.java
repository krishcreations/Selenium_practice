package Loops;

import java.util.Iterator;

public class For_Loop {

	public static void main(String[] args) {
		int a ;
		for (int i = 1;i<=100;i++) {
			System.out.println(i);
		}
		System.out.println("Numbers Printed");
		for(a=2;a<10;a+=2) {
			System.out.println(a);
		}

	}

}
